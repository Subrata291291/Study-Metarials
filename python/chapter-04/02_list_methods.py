#append is use to add form the last
name = ["Apple" , "Banana", 512 , 531.21]
name.append("Guava")
print(name)

#sort is use to list small to big
list = [1,22,5,45,12]
list.sort()
print(list)

#reverse is use to reverse the list
list = [1,22,5,45,12]
list.reverse()
print(list)

#insert is use to add after an order in the list
list = [1,22,5,45,12]
list.insert(3 , 54)
print(list)

#pop is use to delete element from the list
list = [1,22,5,45,12]
list.pop(3) # 3 is the index number from the list (Target value 45)
print(list)

#remove is use to remove element from the list
list = [1,22,5,45,12]
list.remove(5)
print(list)